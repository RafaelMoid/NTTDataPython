# atribuição simples de saldo para 500
saldo = 500
print(saldo)

# saldo aumentado por 200
saldo += 200
print(saldo)

# saldo diminuido por 200
saldo -= 200
print(saldo)

# saldo multiplicado por 2
saldo *= 2
print(saldo)

# saldo dividido por 5
saldo /= 5
print(saldo)

# saldo elevado a 5
saldo **= 2
print(saldo)

# Saldo dividido por 5 e arredondado para cima
saldo //= 5
print(saldo)